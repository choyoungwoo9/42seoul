#include "philosopher.h"

int	main(int argc, char **argv)
{
	t_dinner_info	info;

	if (argc != 5 && argc != 6)
		return (0);
	if (set_dinner(&info, argc, argv) == -1)
		return (0);
	start_dinner(&info);
	observe_dinner(&info);
}
