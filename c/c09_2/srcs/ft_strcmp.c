int	ft_strcmp(char *s1, char *s2)
{
	int	i;
	int	gap;

	i = 0;
	while (*(s1 + i) || *(s2 + i))
	{
		gap = *(s1 + i) - *(s2 + i);
		if (gap != 0)
			return (gap);
		i ++;
	}
	return (*(s1 + i) - *(s2 + i));
}
