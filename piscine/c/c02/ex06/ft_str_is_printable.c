int	ft_str_is_printable(char *str)
{
	unsigned int	i;

	i = 0;
	while (*(str + i))
	{
		if (*(str + i) < ' ' || *(str + i) == 127)
		{
			return (0);
		}
		i ++;
	}
	return (1);
}
