#include<unistd.h>

void	ft_is_negative(int n)
{
	char	nn;
	char	p;

	nn = 'N';
	p = 'P';
	if (n < 0)
	{
		write(1, &nn, 1);
	}
	else
	{
		write(1, &p, 1);
	}
}
